import { ChatMessage as ChatMessageType } from '../hooks/useAIChat';
import { AiSource } from '../../../api/ai.api';
import { useNavigate } from 'react-router-dom';

interface Props {
  message: ChatMessageType;
}

function SourceChip({ source }: { source: AiSource }) {
  const navigate = useNavigate();

  const icon = {
    work_item: (
      <svg className="w-3 h-3" fill="none" stroke="currentColor" viewBox="0 0 24 24">
        <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2}
          d="M9 5H7a2 2 0 00-2 2v12a2 2 0 002 2h10a2 2 0 002-2V7a2 2 0 00-2-2h-2M9 5a2 2 0 002 2h2a2 2 0 002-2M9 5a2 2 0 012-2h2a2 2 0 012 2" />
      </svg>
    ),
    project: (
      <svg className="w-3 h-3" fill="none" stroke="currentColor" viewBox="0 0 24 24">
        <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2}
          d="M3 7v10a2 2 0 002 2h14a2 2 0 002-2V9a2 2 0 00-2-2h-6l-2-2H5a2 2 0 00-2 2z" />
      </svg>
    ),
    sprint: (
      <svg className="w-3 h-3" fill="none" stroke="currentColor" viewBox="0 0 24 24">
        <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2}
          d="M13 10V3L4 14h7v7l9-11h-7z" />
      </svg>
    ),
    milestone: (
      <svg className="w-3 h-3" fill="none" stroke="currentColor" viewBox="0 0 24 24">
        <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2}
          d="M3 21v-4m0 0V5a2 2 0 012-2h6.5l1 1H21l-3 6 3 6H8.5l-1-1H5a2 2 0 00-2 2zm9-13.5V9" />
      </svg>
    ),
    board: (
      <svg className="w-3 h-3" fill="none" stroke="currentColor" viewBox="0 0 24 24">
        <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2}
          d="M9 17V7m0 10a2 2 0 01-2 2H5a2 2 0 01-2-2V7a2 2 0 012-2h2a2 2 0 012 2m0 10a2 2 0 002 2h2a2 2 0 002-2M9 7a2 2 0 012-2h2a2 2 0 012 2m0 10V7m0 10a2 2 0 002 2h2a2 2 0 002-2V7a2 2 0 00-2-2h-2a2 2 0 00-2 2" />
      </svg>
    ),
  }[source.type];

  const handleClick = () => {
    if (source.url) { navigate(source.url); return; }
    if (source.type === 'project') navigate(`/projects/${source.id}`);
    if (source.type === 'board') navigate(`/projects/${source.id}/board`);
  };

  return (
    <button
      onClick={handleClick}
      title={`${source.type.replace('_', ' ')}: ${source.title}`}
      className="inline-flex items-center gap-1 px-2 py-0.5 rounded-full text-xs font-medium
                 bg-primary-50 text-primary-700 border border-primary-200
                 hover:bg-primary-100 transition-colors truncate max-w-[160px]"
    >
      {icon}
      <span className="truncate">{source.title}</span>
    </button>
  );
}

